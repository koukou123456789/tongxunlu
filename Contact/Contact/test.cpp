#include "contact.h"


void menu() {
	printf("*********************************\n");
	printf("**** 1.add        2.delete ******\n");
	printf("**** 3.search     4.modify   ****\n");
	printf("**** 5.show       6.save     ****\n");
	printf("**** 0.exit                  ****\n");
	printf("*********************************\n");
}

int main() {
	int input = 0;


	//创建通讯录，
	struct Contact con;
	InitContact(&con);

	do
	{
		menu();
		printf("请选择：");
		scanf("%d", &input);
		switch (input) {
		case ADD:
			AddContact(&con);
			break;
		case DELETE:
			DelContact(&con);
			break;
		case SEARCH:
			SearchContact(&con);
			break;
		case MODIFY:
			ModifyContact(&con);
			break;
		case SHOW:
			ShowContact(&con);
			break;
		case SAVE:
			SaveContact(&con);
			break;
		case EXIT:
			printf("退出通讯录！\n");
			DestoryContact(&con);
			break;
		default:
			printf("选择错误，请重新选择\n");
			break;
		}
	} while (input);
}


